class Solution {
    public int findPeakElement(int[] nums) {
        
        //check for null input 
        if(nums == null || nums.length ==0)return -1;
        int left = 0;
        int right = nums.length -1;
        while(left < right) {
            int mid = left+(right-left)/2;
            if(nums[mid] > nums[mid+1]) right = mid;
            else left = mid+1;
        }
        return left;
    }
}
//Time complexity O(logn)